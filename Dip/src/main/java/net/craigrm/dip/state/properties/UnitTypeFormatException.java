package net.craigrm.dip.state.properties;

public class UnitTypeFormatException extends IllegalArgumentException {

	private static final long serialVersionUID = 0L;

	public UnitTypeFormatException() {
		super();
	}

	public UnitTypeFormatException(String s) {
		super(s);
	}

	public UnitTypeFormatException(Throwable cause) {
		super(cause);
	}

	public UnitTypeFormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
